package security;

import java.util.List;

public class Values {

	private List<String> value;

	public Values() {
		super();
	}

	public Values(List<String> value) {
		super();
		this.value = value;
	}

	public List<String> getValue() {
		return value;
	}

	public void setValue(List<String> value) {
		this.value = value;
	}
	
	
}
