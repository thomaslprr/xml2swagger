package paths;

public class AdditionalProperties {
	
	private String type;
	
	public AdditionalProperties() {
		super();
	}

	public AdditionalProperties(String type) {
		super();
		this.type = type;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}
